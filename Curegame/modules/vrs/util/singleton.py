import logging

###########################################################################
# Singleton.
###########################################################################

class Singleton(object):
    """
    Subclass to enable singleton, only a single instance of a class is created,
    access shared object via the global instance() class method.
    """
    def __new__(cls, *p, **k):
        if not '_shared_instance' in cls.__dict__:
            cls._shared_instance = object.__new__(cls)
        return cls._shared_instance

    @classmethod
    def instance(cls):
        return cls._shared_instance


